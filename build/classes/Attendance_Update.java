    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
                                                        if(Details.getText().equals("")){
                                                             JOptionPane.showMessageDialog(null,"Please fill in the field\n Thank you");
                                                         }else{
                                                           try{
                                                               
                                                  Nccf_Connect.getConnection();
                                                  con=DriverManager.getConnection("jdbc:mysql://localhost/nccf management system","root","");
                                                  
                                                   String sql = "SELECT * FROM attendance WHERE serial_number = ?";
                                                   
                                                   pst = con.prepareStatement(sql);
                                                  pst.setString(1,Details.getText());
                                                  
                                                   rs = pst.executeQuery();
                                                   if(rs.next()){
          
                                                   String rex1 = rs.getString("serial_number");
                                                   Serial.setText(rex1);
          
         
                                                  String rex2 = rs.getString("name");
                                                  name.setText(rex2);
         
                                                  String rex3 = rs.getString("sex");
                                                  sex.setText(rex3);
         
                                                 String rex4 = rs.getString("batch");
                                                 batch.setText(rex4);
                                                 String rex5 = rs.getString("units");
                                                 Unit.setText(rex5);
                                                 String rex6 = rs.getString("address");
                                                  Adr.setText(rex6);
                                                  String rex7 = rs.getString("phone_number");
                                                  no.setText(rex7);
                                                  String rex8 = rs.getString("Dob");
                                                   Dob.setText(rex8);
                                                   String rex9 = rs.getString("attendance_status");
                                                   status.setText(rex9);
                                                   String rex10 = rs.getString("date");
                                                   Date.setText(rex10);
                                                   String rex11 = rs.getString("time");
                                                    TIME.setText(rex11);
                                                    String rex12 = rs.getString("days");
                                                    Days.setText(rex12);
                                                    
                                                     rs.close();
                                                    pst.close();  
                                                     }else{
                                                       JOptionPane.showMessageDialog(null, "No Membership  Record  Found");
      }
                                                   
                                                           }catch(Exception e){
                                                               JOptionPane.showMessageDialog(null, e);
                                                           }
                                                        }
    }//GEN-LAST:event_searchActionPerformed

